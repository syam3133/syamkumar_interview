 <script type="text/javascript">
        function delete_row(event, id, path) {
            $(".conf_true").on('click', function () {
              
                $.post(path, {
                        'id': id,
                        _token: '{{csrf_token()}}'
                    })
                    .done(function (data) {
                            

                        if (data == 1) {
                              $("#delete_modals").removeClass("in");
                              $(".modal-backdrop").remove();
                              $("#delete_modals").hide();
                            $(event.target || event.srcElement).parents('tr').hide(1000);
                            $(event.target || event.srcElement).parents('card').hide(1000);
    //                     
                        } else {
                              $("#delete_modals").removeClass("in");
                              $(".modal-backdrop").remove();
                              $("#delete_modals").hide();
                          
                        }
                    })
            });
        }
</script>
